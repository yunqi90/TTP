void tt_multiweb_tdcl_frame_copy(tt_Controller_Base base, tt_tdc_FCL_Contr_Frame_Config *contr_frame_config)
{
    ubyte4 prot_state;
    tt_ttp_get_protocol_state(base, (tt_ttp_Protocol_State *)&prot_state);
    if ((prot_state == TT_TTP_ACTIVE) || (prot_state == TT_TTP_PASSIVE))
    {
        tt_tdc_Status_Fr_Copy res;
        tt_multiweb_tdcl_frame_copy_guard += 1;
        if (tt_multiweb_tdcl_frame_copy_guard > 1)
        {
            tt_multiweb_tdcl_frame_copy_error |= 8;
        }
        res = tt_tdc_frame_copy(contr_frame_config);
        tt_multiweb_tdcl_frame_copy_guard -= 1;
        switch (res)
        {
            case TT_TDCOM_FR_COPY_VALIDITY_ERROR:
                tt_multiweb_tdcl_frame_copy_error |= 1;
                break;
            case TT_TDCOM_FR_COPY_STATE_INVALID:
                tt_multiweb_tdcl_frame_copy_error |= 2;
                break;
            case TT_TDCOM_FR_COPY_INT_SKIPPED:
                tt_multiweb_tdcl_frame_copy_error |= 4;
                break;
            default:
                break;
        }
    }
    else
    {
        if (prot_state == TT_TTP_COLDSTART)
        {
            tt_ttp_update_lifesign(base);
        }
    }
}